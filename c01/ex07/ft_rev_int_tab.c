void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int j;

	size = size - 1;
	i = 0;
	while (i < size)
	{
		j = tab[i];
		tab[i] = tab[size];
		tab[size] = j;
		i++;
		size--;
	}
}
